# Main Trading Agent
class HyperliquidTradingAgent:
    """Autonomous trading agent for Hyperliquid DEX."""

    def __init__(
        self,
        starting_capital: float,
        risk_params: Optional[RiskParams] = None,
        db_path: str = "hyperliquid_trading_state.db",
        testnet: bool = True,
    ):
        """Initialize trading agent.

        Args:
            starting_capital: Starting capital in USD
            risk_params: Risk management parameters
            db_path: Path to state database
            testnet: Use testnet (default: True for safety)
        """
        self.starting_capital = starting_capital
        self.testnet = testnet

        # Initialize managers
        self.risk_params = risk_params or RiskParams()
        self.risk_manager = RiskManager(self.risk_params, starting_capital)
        self.state_manager = StateManager(db_path)

        # Track current capital (will be updated from account balance)
        self.current_capital = starting_capital

    async def get_current_capital(self) -> float:
        """Get current account capital from Hyperliquid.

        Returns:
            Current capital in USD
        """
        if not HYPERLIQUID_TOOLS_AVAILABLE:
            return self.current_capital

        balance = check_account_balance(testnet=self.testnet)
        if "error" not in balance:
            self.current_capital = balance["account_value"]
        return self.current_capital

    async def analyze_market(self, coin: str) -> MarketAnalysis:
        """Analyze a market using comprehensive data and LLM reasoning.

        Args:
            coin: Trading pair to analyze

        Returns:
            Market analysis
        """
        # Gather market data
        market_data = get_market_data(coin, testnet=self.testnet)
        order_book = get_order_book(coin, depth=10, testnet=self.testnet)
        funding_rate = get_funding_rate(coin, testnet=self.testnet)

        # Use LLM to analyze
        analysis = await analyze_market_conditions(
            coin=coin,
            market_data=market_data,
            order_book=order_book,
            funding_rate=funding_rate,
        )

        return analysis

    async def develop_strategy(
        self,
        markets: list[str],
        risk_tolerance: Literal["conservative", "moderate", "aggressive"] = "moderate",
        time_horizon: Literal["short", "medium", "long"] = "medium",
    ) -> TradingStrategy:
        """Develop an autonomous trading strategy.

        Args:
            markets: Markets to consider
            risk_tolerance: Risk tolerance level
            time_horizon: Trading time horizon

        Returns:
            Developed trading strategy
        """
        # Analyze each market
        market_analyses = []
        for coin in markets:
            analysis = await self.analyze_market(coin)
            market_analyses.append(analysis.model_dump())

        # Get current capital
        capital = await self.get_current_capital()

        # Have LLM develop strategy
        strategy_plan = await develop_autonomous_strategy(
            markets=markets,
            market_analyses=market_analyses,
            capital=capital,
            risk_tolerance=risk_tolerance,
            time_horizon=time_horizon,
        )

        # Convert to TradingStrategy
        strategy = TradingStrategy(
            type=strategy_plan.strategy_type,
            name=strategy_plan.strategy_name,
            description=strategy_plan.description,
            markets=strategy_plan.markets,
            entry_rules=strategy_plan.entry_criteria,
            exit_rules=strategy_plan.exit_criteria,
            risk_per_trade=self.risk_params.stop_loss_pct,
            max_positions=self.risk_params.max_positions,
            use_stop_loss=self.risk_params.use_stop_loss,
            use_take_profit=self.risk_params.use_take_profit,
        )

        return strategy

    async def execute_strategy_iteration(
        self,
        strategy: TradingStrategy,
    ) -> dict[str, Any]:
        """Execute one iteration of a trading strategy.

        Args:
            strategy: Trading strategy to execute

        Returns:
            Iteration results including actions taken
        """
        results = {
            "timestamp": datetime.now(),
            "actions": [],
            "analyses": [],
            "errors": [],
        }

        # Get current state
        capital = await self.get_current_capital()
        current_positions = get_open_positions(testnet=self.testnet)

        # Analyze each market in strategy
        for coin in strategy.markets:
            try:
                # Analyze market
                analysis = await self.analyze_market(coin)
                results["analyses"].append(analysis.model_dump())

                # Check if we should trade this market
                # First, check if we already have a position
                existing_position = next((p for p in current_positions if p["coin"] == coin), None)

                if existing_position:
                    # Check if we should close the position
                    if analysis.recommendation.lower() == "sell" and existing_position["side"] == "long":
                        results["actions"].append({"action": "close_position", "coin": coin, "reason": "Exit signal from analysis"})
                        # Close position
                        close_result = close_position(coin, testnet=self.testnet)
                        if close_result.get("success"):
                            self.state_manager.record_trade_exit(
                                coin=coin,
                                exit_price=analysis.key_levels.get("current", 0),
                                exit_reason="Strategy exit signal",
                            )
                    continue  # Skip to next market if we have a position

                # Check if we're at max positions
                if len(current_positions) >= strategy.max_positions:
                    continue

                # Consider new position
                if analysis.recommendation.lower() == "buy" and analysis.confidence > 0.6:
                    # Calculate position size
                    entry_price = analysis.key_levels.get("current", 0)
                    stop_loss = self.risk_manager.calculate_stop_loss(entry_price, "long")
                    position_size = self.risk_manager.calculate_position_size(
                        capital=capital,
                        entry_price=entry_price,
                        stop_loss_price=stop_loss,
                        risk_pct=strategy.risk_per_trade,
                    )

                    size_usd = position_size * entry_price

                    # Risk check
                    risk_check = self.risk_manager.check_new_position(
                        coin=coin,
                        size_usd=size_usd,
                        entry_price=entry_price,
                        stop_loss_price=stop_loss,
                        current_capital=capital,
                        current_positions=len(current_positions),
                    )

                    if risk_check.approved and not risk_check.requires_approval:
                        # Execute trade
                        take_profit = self.risk_manager.calculate_take_profit(entry_price, "long", risk_reward_ratio=2.0)

                        order_result = place_market_order(
                            coin=coin,
                            side="buy",
                            size=position_size,
                            testnet=self.testnet,
                        )

                        if order_result.get("success"):
                            # Record trade
                            self.state_manager.record_trade_entry(
                                coin=coin,
                                side="long",
                                entry_price=entry_price,
                                size=position_size,
                                strategy=strategy.name,
                                entry_reason=analysis.reasoning,
                                stop_loss=stop_loss,
                                take_profit=take_profit,
                            )

                            results["actions"].append(
                                {
                                    "action": "opened_position",
                                    "coin": coin,
                                    "size": position_size,
                                    "entry_price": entry_price,
                                    "stop_loss": stop_loss,
                                    "take_profit": take_profit,
                                }
                            )

            except Exception as e:
                results["errors"].append({"coin": coin, "error": str(e)})

        return results

    async def run_autonomous_trading(
        self,
        markets: list[str],
        duration_hours: int = 24,
        check_interval_minutes: int = 15,
    ) -> dict[str, Any]:
        """Run autonomous trading for a specified duration.

        Args:
            markets: Markets to trade
            duration_hours: How long to run (hours)
            check_interval_minutes: Minutes between strategy checks

        Returns:
            Summary of trading session
        """
        # Develop strategy
        strategy = await self.develop_strategy(markets=markets)

        print(f"🤖 Autonomous Trading Started")
        print(f"Strategy: {strategy.name}")
        print(f"Markets: {', '.join(strategy.markets)}")
        print(f"Duration: {duration_hours}h, Check Interval: {check_interval_minutes}m")
        print(f"Starting Capital: ${self.current_capital:.2f}")

        start_time = datetime.now()
        iterations = 0

        while (datetime.now() - start_time).total_seconds() < duration_hours * 3600:
            iterations += 1
            print(f"\n--- Iteration {iterations} ---")

            # Execute strategy iteration
            results = await self.execute_strategy_iteration(strategy)

            # Log results
            if results["actions"]:
                print(f"Actions taken: {len(results['actions'])}")
                for action in results["actions"]:
                    print(f"  - {action}")

            if results["errors"]:
                print(f"Errors: {len(results['errors'])}")

            # Save performance snapshot
            capital = await self.get_current_capital()
            metrics = self.state_manager.calculate_performance_metrics()

            snapshot = PerformanceSnapshot(
                timestamp=datetime.now(),
                capital=capital,
                total_pnl=capital - self.starting_capital,
                daily_pnl=0,  # Would need to track this properly
                open_positions=len(get_open_positions(testnet=self.testnet)),
                total_trades=metrics["total_trades"],
                win_rate=metrics["win_rate"],
                sharpe_ratio=0.0,  # Would need price history to calculate
                max_drawdown=0.0,  # Would need to track
            )
            self.state_manager.save_performance_snapshot(snapshot)

            # Wait for next iteration
            await asyncio.sleep(check_interval_minutes * 60)

        # Final summary
        final_capital = await self.get_current_capital()
        final_metrics = self.state_manager.calculate_performance_metrics()

        return {
            "starting_capital": self.starting_capital,
            "ending_capital": final_capital,
            "total_pnl": final_capital - self.starting_capital,
            "total_pnl_pct": ((final_capital - self.starting_capital) / self.starting_capital * 100),
            "iterations": iterations,
            "metrics": final_metrics,
        }


# Main entry functions
async def trade_autonomous(
    starting_capital: float,
    markets: list[str],
    risk_params: Optional[RiskParams] = None,
    duration_hours: int = 24,
    testnet: bool = True,
) -> dict[str, Any]:
    """Start autonomous trading session.

    Args:
        starting_capital: Starting capital in USD
        markets: Markets to trade
        risk_params: Risk management parameters
        duration_hours: Trading duration in hours
        testnet: Use testnet (default: True)

    Returns:
        Trading session summary
    """
    agent = HyperliquidTradingAgent(
        starting_capital=starting_capital,
        risk_params=risk_params,
        testnet=testnet,
    )

    return await agent.run_autonomous_trading(markets=markets, duration_hours=duration_hours)


