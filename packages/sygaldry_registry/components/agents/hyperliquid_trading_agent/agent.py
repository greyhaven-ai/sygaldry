# Response models
class MarketAnalysis(BaseModel):
    """Market analysis result."""

    coin: str = Field(..., description="Trading pair analyzed")
    trend: Literal["bullish", "bearish", "neutral"] = Field(..., description="Current trend")
    trend_strength: SignalStrength = Field(..., description="Trend strength")
    key_levels: dict[str, float] = Field(..., description="Key support/resistance levels")
    sentiment: str = Field(..., description="Market sentiment analysis")
    indicators: dict[str, Any] = Field(..., description="Technical indicator values")
    recommendation: str = Field(..., description="Trading recommendation")
    confidence: float = Field(..., description="Analysis confidence 0-1")
    reasoning: str = Field(..., description="Detailed reasoning for analysis")


class AutonomousStrategyPlan(BaseModel):
    """Autonomous strategy plan generated by the agent."""

    strategy_name: str = Field(..., description="Strategy name")
    strategy_type: StrategyType = Field(..., description="Strategy type")
    description: str = Field(..., description="Strategy description")
    markets: list[str] = Field(..., description="Markets to trade")
    entry_criteria: str = Field(..., description="Entry criteria")
    exit_criteria: str = Field(..., description="Exit criteria")
    risk_management: str = Field(..., description="Risk management approach")
    expected_performance: str = Field(..., description="Expected performance characteristics")
    reasoning: str = Field(..., description="Why this strategy is appropriate")


class TradeDecision(BaseModel):
    """Decision to execute a trade."""

    should_trade: bool = Field(..., description="Whether to execute the trade")
    coin: str = Field(..., description="Trading pair")
    action: Literal["buy", "sell", "hold", "close"] = Field(..., description="Action to take")
    size_usd: Optional[float] = Field(None, description="Trade size in USD")
    entry_type: Literal["market", "limit"] = Field("market", description="Order type")
    limit_price: Optional[float] = Field(None, description="Limit price if limit order")
    stop_loss: Optional[float] = Field(None, description="Stop loss price")
    take_profit: Optional[float] = Field(None, description="Take profit price")
    reasoning: str = Field(..., description="Reasoning for the decision")
    confidence: float = Field(..., description="Confidence in decision 0-1")


