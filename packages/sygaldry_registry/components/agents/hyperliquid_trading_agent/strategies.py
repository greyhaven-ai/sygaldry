"""Trading strategies for Hyperliquid trading agent.

This module implements various trading strategies including trend following,
mean reversion, funding arbitrage, and more.
"""

from __future__ import annotations

from enum import Enum
from typing import Any, Literal, Optional
from pydantic import BaseModel, Field


class StrategyType(str, Enum):
    """Available trading strategy types."""

    AUTONOMOUS = "autonomous"  # Agent develops its own strategy
    TREND_FOLLOWING = "trend_following"
    MEAN_REVERSION = "mean_reversion"
    FUNDING_ARBITRAGE = "funding_arbitrage"
    BREAKOUT = "breakout"
    MOMENTUM = "momentum"
    PAIRS_TRADING = "pairs_trading"
    GRID_TRADING = "grid_trading"
    CUSTOM = "custom"


class TimeFrame(str, Enum):
    """Trading timeframes."""

    M1 = "1m"
    M5 = "5m"
    M15 = "15m"
    M30 = "30m"
    H1 = "1h"
    H4 = "4h"
    D1 = "1d"


class SignalStrength(str, Enum):
    """Signal strength levels."""

    VERY_WEAK = "very_weak"
    WEAK = "weak"
    NEUTRAL = "neutral"
    STRONG = "strong"
    VERY_STRONG = "very_strong"


class TradingSignal(BaseModel):
    """Trading signal generated by a strategy."""

    coin: str = Field(..., description="Trading pair")
    action: Literal["buy", "sell", "hold", "close"] = Field(..., description="Recommended action")
    strength: SignalStrength = Field(..., description="Signal strength")
    confidence: float = Field(..., description="Confidence score 0-1")
    entry_price: Optional[float] = Field(None, description="Recommended entry price")
    stop_loss: Optional[float] = Field(None, description="Recommended stop loss price")
    take_profit: Optional[float] = Field(None, description="Recommended take profit price")
    position_size: Optional[float] = Field(None, description="Recommended position size")
    reasoning: str = Field(..., description="Explanation for the signal")
    indicators: dict[str, Any] = Field(default_factory=dict, description="Supporting indicator values")
    timestamp: int = Field(..., description="Signal generation timestamp")


class TradingStrategy(BaseModel):
    """Trading strategy configuration."""

    type: StrategyType = Field(..., description="Strategy type")
    name: str = Field(..., description="Strategy name")
    description: str = Field(..., description="Strategy description")
    markets: list[str] = Field(default_factory=list, description="Markets to trade (e.g., ['BTC', 'ETH'])")
    timeframe: TimeFrame = Field(default=TimeFrame.H1, description="Primary timeframe")

    # Strategy parameters
    parameters: dict[str, Any] = Field(default_factory=dict, description="Strategy-specific parameters")

    # Entry/Exit rules
    entry_rules: Optional[str] = Field(None, description="Entry conditions description")
    exit_rules: Optional[str] = Field(None, description="Exit conditions description")

    # Risk parameters
    risk_per_trade: float = Field(0.02, description="Risk per trade as fraction (e.g., 0.02 = 2%)")
    max_positions: int = Field(3, description="Maximum concurrent positions")
    use_stop_loss: bool = Field(True, description="Use stop losses")
    use_take_profit: bool = Field(True, description="Use take profit orders")

    # Advanced settings
    use_trailing_stop: bool = Field(False, description="Use trailing stop loss")
    trailing_stop_pct: float = Field(0.02, description="Trailing stop percentage")
    partial_close_enabled: bool = Field(False, description="Enable partial position closes")
    pyramiding_enabled: bool = Field(False, description="Allow adding to winning positions")


class StrategyPerformance(BaseModel):
    """Strategy performance metrics."""

    strategy_name: str = Field(..., description="Strategy name")
    total_trades: int = Field(0, description="Total number of trades")
    winning_trades: int = Field(0, description="Number of winning trades")
    losing_trades: int = Field(0, description="Number of losing trades")
    win_rate: float = Field(0.0, description="Win rate percentage")
    total_pnl: float = Field(0.0, description="Total profit/loss")
    average_win: float = Field(0.0, description="Average winning trade")
    average_loss: float = Field(0.0, description="Average losing trade")
    profit_factor: float = Field(0.0, description="Profit factor (gross profit / gross loss)")
    sharpe_ratio: float = Field(0.0, description="Sharpe ratio")
    max_drawdown: float = Field(0.0, description="Maximum drawdown")
    max_drawdown_pct: float = Field(0.0, description="Maximum drawdown percentage")
    average_trade_duration_hours: float = Field(0.0, description="Average trade duration in hours")


# Pre-defined strategy templates
def get_trend_following_strategy(
    markets: list[str],
    timeframe: TimeFrame = TimeFrame.H1,
    risk_per_trade: float = 0.02,
) -> TradingStrategy:
    """Get a trend following strategy configuration.

    This strategy follows strong trends using moving averages and momentum indicators.

    Args:
        markets: List of markets to trade
        timeframe: Trading timeframe
        risk_per_trade: Risk per trade as fraction

    Returns:
        Configured TradingStrategy
    """
    return TradingStrategy(
        type=StrategyType.TREND_FOLLOWING,
        name="Trend Following",
        description="Follow strong trends using MA crossovers and momentum",
        markets=markets,
        timeframe=timeframe,
        parameters={
            "fast_ma_period": 20,
            "slow_ma_period": 50,
            "momentum_period": 14,
            "trend_strength_threshold": 0.7,
            "volume_confirmation": True,
        },
        entry_rules="Enter long when fast MA crosses above slow MA and momentum is positive. Enter short when fast MA crosses below slow MA and momentum is negative.",
        exit_rules="Exit when opposite signal occurs or stop loss/take profit is hit.",
        risk_per_trade=risk_per_trade,
        max_positions=3,
        use_stop_loss=True,
        use_take_profit=True,
        use_trailing_stop=True,
        trailing_stop_pct=0.03,
    )


