def get_grid_trading_strategy(
    markets: list[str],
    grid_levels: int = 10,
    grid_spacing_pct: float = 0.01,
) -> TradingStrategy:
    """Get a grid trading strategy configuration.

    This strategy places buy and sell orders at regular intervals (grid).

    Args:
        markets: List of markets to trade
        grid_levels: Number of grid levels
        grid_spacing_pct: Spacing between grid levels as percentage

    Returns:
        Configured TradingStrategy
    """
    return TradingStrategy(
        type=StrategyType.GRID_TRADING,
        name="Grid Trading",
        description="Place orders at regular price intervals to profit from ranging markets",
        markets=markets,
        timeframe=TimeFrame.M15,
        parameters={
            "grid_levels": grid_levels,
            "grid_spacing_pct": grid_spacing_pct,
            "base_order_size": 0.01,
            "range_detection": True,
        },
        entry_rules="Place buy orders below current price and sell orders above at fixed intervals.",
        exit_rules="Each order pair (buy + sell) takes profit automatically.",
        risk_per_trade=0.01,
        max_positions=grid_levels,
        use_stop_loss=False,  # Grid trading typically doesn't use stops
        use_take_profit=True,
    )


# Strategy selection helper
def get_strategy_by_type(
    strategy_type: StrategyType,
    markets: list[str],
    **kwargs,
) -> TradingStrategy:
    """Get a pre-configured strategy by type.

    Args:
        strategy_type: Type of strategy to get
        markets: Markets to trade
        **kwargs: Additional parameters to pass to strategy

    Returns:
        Configured TradingStrategy
    """
    strategy_map = {
        StrategyType.TREND_FOLLOWING: get_trend_following_strategy,
        StrategyType.MEAN_REVERSION: get_mean_reversion_strategy,
        StrategyType.FUNDING_ARBITRAGE: get_funding_arbitrage_strategy,
        StrategyType.BREAKOUT: get_breakout_strategy,
        StrategyType.MOMENTUM: get_momentum_strategy,
        StrategyType.GRID_TRADING: get_grid_trading_strategy,
    }

    if strategy_type not in strategy_map:
        raise ValueError(f"Unknown strategy type: {strategy_type}")

    return strategy_map[strategy_type](markets=markets, **kwargs)
